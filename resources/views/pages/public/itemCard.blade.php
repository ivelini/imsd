        <script>
            document.addEventListener("DOMContentLoaded", function () {
                function toggleContent() {
                    var content = document.querySelectorAll("#description > .section-content");
                    var btn = document.querySelector(".show-hide-btn-description");
                    var btnText = btn.querySelector(".btn-text");
                    var svgInBtn = btn.querySelector("svg");

                    content.forEach(function (p, index) {
                        if (index === 0) {
                            return; // Пропускаем первый параграф, чтобы он всегда оставался видимым
                        }
                        p.style.display = p.style.display === "none" ? "block" : "none";
                    });

                    btnText.innerHTML = btnText.innerHTML === "Показать всё" ? "Скрыть" : "Показать всё";
                    svgInBtn.classList.toggle("rotated");
                }

                if (window.innerWidth <= 599) {
                    var content = document.querySelectorAll("#description > .section-content");
                    var btn = document.querySelector(".show-hide-btn-description");

                    content.forEach(function (p, index) {
                        p.style.display = index === 0 ? "block" : "none";
                    });

                    btn.addEventListener("click", toggleContent);

                    window.addEventListener("resize", function () {
                        if (window.innerWidth <= 599) {
                            toggleContent();
                        } else {
                            // Если ширина больше 599px, показываем все параграфы
                            content.forEach(function (p) {
                                p.style.display = "block";
                            });
                            btn.innerHTML = "Скрыть";
                        }
                    });
                }
            });
        </script>
